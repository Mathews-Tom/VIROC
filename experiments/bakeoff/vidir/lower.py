"""Throwaway VidIR -> Manim lowering for the M1 bake-off.

NOT the compiler. A single, grammar-generic lowering that turns any validated
``pipeline`` storyboard into a byte-deterministic Manim ``Scene`` source string.
The point of the bake-off is that this ~one screen of generic machinery, written
once, lowers *all five* topics — the author only edits declarative YAML.

Scope guard: discarded after the gate. Milestone M6/M9 build the real, per-grammar
resolver + adapter; this must never be imported by ``src/viroc``.
"""

from __future__ import annotations

from schema import SemanticIR, load

# Visual language shared with the hand-written Manim references so the bake-off
# compares authoring ergonomics, not aesthetics.
TYPE_COLOR: dict[str, str] = {
    "data_source": "#3b82f6",
    "intermediate": "#14b8a6",
    "model": "#a855f7",
    "storage": "#22c55e",
    "service": "#f59e0b",
    "user": "#94a3b8",
}
KIND_COLOR: dict[str, str] = {
    "flow": "#8b949e",
    "split": "#60a5fa",
    "transform": "#34d399",
    "store": "#4ade80",
    "merge": "#fbbf24",
    "compare": "#f472b6",
}


def class_name(topic: str) -> str:
    """``"rag-pipeline"`` -> ``"RagPipeline"`` (a valid Manim Scene class name)."""
    return "".join(part.capitalize() for part in topic.replace("_", "-").split("-"))


def lower_storyboard(ir: SemanticIR, topic: str) -> str:
    """Lower the first scene of ``ir`` to a renderable Manim source string."""
    scene = ir.scenes[0]
    label_of = {entity.id: entity.label for entity in ir.entities}
    type_of = {entity.id: entity.type for entity in ir.entities}

    specs = [
        (node, label_of[node], TYPE_COLOR[type_of[node]]) for node in scene.nodes
    ]
    edges = [
        (edge.from_, edge.to, KIND_COLOR[edge.kind]) for edge in scene.edges
    ]

    spec_lines = "\n".join(
        f"            ({nid!r}, {label!r}, {color!r})," for nid, label, color in specs
    )
    edge_lines = "\n".join(
        f"            ({a!r}, {b!r}, {color!r})," for a, b, color in edges
    )
    narration = scene.narration or ""

    return _TEMPLATE.format(
        topic=topic,
        cls=class_name(topic),
        title=ir.video.title,
        spec_lines=spec_lines,
        edge_lines=edge_lines,
        narration=narration,
    )


def lower_file(yaml_path: str, topic: str) -> str:
    """Load + validate + lower a storyboard file to Manim source."""
    return lower_storyboard(load(yaml_path), topic)


# The lowering owns layout generically: adjacent forward edges become straight
# arrows between box faces; skips and back-edges become curved arrows below the
# row. This is what lets one template serve every pipeline topology.
_TEMPLATE = '''\
# Auto-generated by experiments/bakeoff/vidir/lower.py from {topic}.vidir.yaml.
# Throwaway M1 lowering output — NOT the compiler. Do not edit by hand.
from manim import *


class {cls}(Scene):
    def construct(self) -> None:
        self.camera.background_color = "#0e1116"
        title = Text({title!r}, font_size=30, color="#e6edf3").to_edge(UP)
        self.play(FadeIn(title))

        specs = [
{spec_lines}
        ]
        boxes = []
        for _id, _label, _color in specs:
            rect = RoundedRectangle(
                corner_radius=0.12, width=2.4, height=1.0,
                color=_color, fill_color=_color, fill_opacity=0.18, stroke_width=2,
            )
            label = Text(_label, font_size=18, color="#e6edf3")
            if label.width > rect.width - 0.3:
                label.scale_to_fit_width(rect.width - 0.3)
            label.move_to(rect.get_center())
            boxes.append(VGroup(rect, label))

        row = VGroup(*boxes).arrange(RIGHT, buff=0.8)
        if row.width > 12.5:
            row.scale_to_fit_width(12.5)
        row.next_to(title, DOWN, buff=1.2)
        index = {{spec[0]: i for i, spec in enumerate(specs)}}

        for box in boxes:
            self.play(FadeIn(box, shift=UP * 0.2), run_time=0.35)

        edges = [
{edge_lines}
        ]
        connectors = []
        for _a, _b, _color in edges:
            ai, bi = index[_a], index[_b]
            if bi - ai == 1:
                conn = Arrow(
                    boxes[ai].get_right(), boxes[bi].get_left(),
                    buff=0.12, stroke_width=3, color=_color,
                )
                connectors.append(("arrow", conn))
            else:
                angle = -TAU / 6 if bi > ai else TAU / 6
                conn = CurvedArrow(
                    boxes[ai].get_bottom(), boxes[bi].get_bottom(),
                    angle=angle, stroke_width=3, color=_color,
                )
                connectors.append(("curve", conn))

        for kind, conn in connectors:
            self.play(GrowArrow(conn) if kind == "arrow" else Create(conn), run_time=0.3)

        narration = {narration!r}
        if narration:
            caption = Text(narration, font_size=18, color="#8b949e")
            if caption.width > 12.0:
                caption.scale_to_fit_width(12.0)
            caption.next_to(row, DOWN, buff=1.0)
            self.play(FadeIn(caption))
        self.wait(1.0)
'''
